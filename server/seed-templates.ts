async function seedTemplates() {
  console.log('üå± Seeding templates...');
  
  try {
    // Clear existing templates (optional)
    await db.delete(templates);
    console.log('‚úÖ Cleared existing templates');
    
    // Insert all templates
    for (const template of REPLIT_TEMPLATES) {
      await db.insert(templates).values(template);
      console.log(`‚úÖ Added template: ${template.name}`);
    }
    
    console.log(`‚úÖ Successfully seeded ${REPLIT_TEMPLATES.length} templates!`);
  } catch (error) {
    console.error('‚ùå Error seeding templates:', error);
    throw error;
  }
}

// Run the seed function
seedTemplates()
  .then(() => {
    console.log('‚úÖ Template seeding completed!');
    process.exit(0);
  })
  .catch((error) => {
    console.error('‚ùå Template seeding failed:', error);
    process.exit(1);
  });